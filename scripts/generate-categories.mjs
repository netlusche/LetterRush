let total = 0;
for (const [id, { kategorie, words }] of Object.entries(CATEGORIES)) {
  const seen = new Set();
  const eintraege = [];
  for (const raw of words) {
    const wort = raw.trim();
    if (!wort || seen.has(wort.toLowerCase())) continue;
    seen.add(wort.toLowerCase());
    eintraege.push({ wort, anfangsbuchstabe: firstLetter(wort) });
  }
  const json = JSON.stringify({ kategorie, eintraege }, null, 2) + '\n';
  writeFileSync(join(OUT_DIR, `${id}.json`), json, 'utf8');
  total += eintraege.length;

  // Coverage report: letters with >= 5 entries.
  const counts = {};
  for (const e of eintraege) counts[e.anfangsbuchstabe] = (counts[e.anfangsbuchstabe] || 0) + 1;
  const strong = Object.entries(counts).filter(([, n]) => n >= 5).map(([l]) => l).sort();
  console.log(`${id.padEnd(10)} ${String(eintraege.length).padStart(4)} entries | >=5: ${strong.join('')}`);
}
console.log(`\nTotal: ${total} entries across ${Object.keys(CATEGORIES).length} categories.`);
